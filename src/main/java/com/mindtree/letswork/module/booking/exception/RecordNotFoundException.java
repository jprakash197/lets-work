package com.mindtree.letswork.module.booking.exception;

public class RecordNotFoundException extends PaymentApplicationException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public RecordNotFoundException() {
		super();
	}

	public RecordNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public RecordNotFoundException(String arg0) {
		super(arg0);
	}

	public RecordNotFoundException(Throwable arg0) {
		super(arg0);
	}
	
	

}
