package com.mindtree.letswork.module.booking.exception;

public class DateServiceException extends PaymentApplicationException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public DateServiceException() {
		super();
	}

	public DateServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DateServiceException(String arg0) {
		super(arg0);
	}

	public DateServiceException(Throwable arg0) {
		super(arg0);
	}
	
	

}
